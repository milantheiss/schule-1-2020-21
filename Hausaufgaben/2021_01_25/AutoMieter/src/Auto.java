public class Auto {
    private String marke;
    private String modell;
    private Mieter meinMieter;

    public String getMarke() {
        return marke;
    }

    public void setMarke(String marke) {
        this.marke = marke;
    }

    public String getModell() {
        return modell;
    }

    public void setModell(String modell) {
        this.modell = modell;
    }

    public Mieter getMeinMieter() {
        return meinMieter;
    }

    public void setMeinMieter(Mieter meinMieter) {
        this.meinMieter = meinMieter;
    }
}
