public class Automat{
    private String typ;
    private double kassenstand;
    private Firma meineFirma;

    public String getTyp() {
        return typ;
    }

    public void setTyp(String typ) {
        this.typ = typ;
    }

    public double getKassenstand() {
        return kassenstand;
    }

    public void setKassenstand(double kassenstand) {
        this.kassenstand = kassenstand;
    }

    public Firma getMeineFirma() {
        return meineFirma;
    }

    public void setMeineFirma(Firma meineFirma) {
        this.meineFirma = meineFirma;
    }
}
